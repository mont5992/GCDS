 <h3>How do I access the material?</h3>
          <p>
            First, you must register. Depending on your membership level, you will then have access to course video teachings, books, articles and audios,
            all available online. Courses are found by selecting the “Courses” tab.
            All other learning material can be found in the Library, by selecting the “Library” tab.
          </p>

 <h3>What is the Omega Credential?</h3>
          <p>
            The omega credential is the 8th crowning credential, eligible to students who have completed all seven ministry tracks.
            <br>The omega credential is a three day face-to-face intensive with one of the board of governors of the GCDS in your region.
            The cost is $495 plus travel and lodging.
          </p>


          <h3>How do I apply for the Omega Credential?</h3>
          <p>
            When students have completed all 7 tracks, they are eligible to apply for the most prestigious credential, Omega.
            <br>Once the application is reviewed, you will be notified regarding approval, and the Omega Credential is scheduled.
          </p>

<h3>HOW DO I CANCEL MY ACCOUNT?</h3>
          <p>
            To cancel your account, first log in and visit your Billing & Subscriptions Settings page.
            From there, change your <strong>Subscription</strong> to <strong>DELETE Subscription</strong>.
            This will halt billing and downgrade your account to a free account.
            If you have any problems with this at all, don’t hesitate to email
            <a href="mailto: olivia@gcnw.tv">olivia@gcnw.tv</a> for assistance.
          </p>

          <h3>WILL THE GLOBAL CHURCH DIVINITY SCHOOL BE ADDING ANY NEW COURSES?</h3>
          <p>The Global Church Divinity School will be continually adding courses in the future. When viewing the “Courses” page you can see which course will be Coming Soon.</p>

