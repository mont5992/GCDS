        <h1>Frequently Asked Questions</h1>
        <h3>How do I become a member?</h3>
          <p>
            In order to become a member, simply go to <a href="https://www.gcds.tv">www.gcds.tv</a> and click on the Membership tab. You will then have the opportunity to choose the kind of membership that best fits you and your ministry.
          </p>

<h3>How Do I Open My Local Divinity School Campus?</h3>
          <p>
            It is very easy for your Global Church Divinity School campus to be started.
            After you choose your name during the registration process and you are logged into your account:
            1) click on the name of the campus in the top right of the navigational bar inside the global church classroom;
            2) create a submaster account;
            3) upload the names under that submaster account;
            4) students begin classes.
            <br><br>
            We have also provided a complete press release packet.
            It is also located in the same area where you create submitter account.
            This PR packet will help you to get the word and build your divinity school.
          </p>

          <h3>WHO CAN APPLY FOR A SCHOLARSHIP?</h3>
          <p>Global Church Divinity School Scholarships are ONLY made available for leaders who primarily live in developing countries.</p>

 <h3>How do I apply for a scholarship?</h3>
          <p>
            Go to the membership tab, scroll to the bottom of the page, click on the button “Apply for a GCN Scholarship.”
            Fill in all requested information. Be sure to include your country in the location field and your current financial status.
          </p>

<h3>When will I know if my scholarship was granted?</h3>
          <p>Each month scholarships will be approved or denied. You will be sent a letter to the email address provided on your application.</p>

          <h3>WHERE DO I TAKE THE COURSES?</h3>
          <p>All courses are offered online; simply select the Classroom tab. On the courses page, select any course by clicking on the blue "watch" button.</p>

<h3>Can I view the courses off line?</h3>
<p>Classes are only available for online viewing.</p>


          <h3>WHAT FORMS OF PAYMENT DO YOU ACCEPT?</h3>
